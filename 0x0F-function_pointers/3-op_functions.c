#include <stdlib.h>

/**
 * op_add - a function that adds two integars
 *
 * @a: the first integar
 * @b: the second integar
 *
 * Return: the sum of the two integars
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function that subtracts an integar
 *          from another
 *
 * @a: the first integar
 * @b: the second integar
 *
 * Return: deduct b from a.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies two integars
 *
 * @a: the first integar
 * @b: the second integar
 *
 * Return: multiply a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divides a with b
 *
 * @a: the first integar
 * @b: the second integar
 *
 * Return: a divided by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns the remaider
 *          after divideing a with b
 *
 * @a: the first integar
 * @b: the second integar
 *
 * Return: the remainder aftter a is divided by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}



